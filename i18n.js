  function normalize(code) {
    if (!code) return null;
    code = String(code).toLowerCase().split('-')[0].split('_')[0];
    return SUPPORTED.indexOf(code) >= 0 ? code : null;
  }

  function detect() {
    // 1) явный выбор пользователя
    try {
      var saved = localStorage.getItem('tcLang');
      if (saved && saved !== 'auto') {
        var n = normalize(saved);
        if (n) return n;
      }
    } catch (e) {}
    // 2) языки браузера
    var cands = [];
    if (navigator.languages && navigator.languages.length) cands = cands.concat(navigator.languages);
    if (navigator.language) cands.push(navigator.language);
    for (var i = 0; i < cands.length; i++) {
      var n2 = normalize(cands[i]);
      if (n2) return n2;
    }
    // 3) серверный дефолт (APP_LANG), затем общий фолбэк
    var srv = normalize(global.APP_DEFAULT_LANG);
    return srv || FALLBACK;
  }

